'''
1. 1/1  => 1 => 1 = 1
2. 1/2 2/1  => 2 => 3 = 1+2
3. 3/1 2/2 1/3 => 3 => 6 = 1+2+3
4. 1/4 2/3 3/2 4/1 => 4 => 10 = 1+2+3+4
5. 5/1 4/2 3/3 2/4 1/5 => 5 => 15 = 1+2+3+4+5

n(n+1)/2
'''

n = int(input())
k = 1 # line
while 1:
    if (k-1)*k//2 <= n <= (k * (k + 1))//2:
        break
    else:
        k+=1

x = n - (k-1) * k // 2
ja, mo = 0,0

# 짝수
if k % 2 ==0:
    ja = x
    mo = k+1-x
    
# 홀수
if k % 2 !=0:
    ja = k+1-x
    mo = x

print(ja ,end="/")
print(mo)





