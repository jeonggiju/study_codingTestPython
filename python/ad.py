import sys

input = sys.stdin.readline

num = int(input())
result = []

for i in range(num):
    std = input().rstrip()
    st = []

    for j in range(len(std)):
        if std[j] == "(":
            st.append("(")
        else:
            if st:
                st.pop()
            else:
                st.append("-")
                break


    if len(st) == 0 :
        result.append("YES")
    elif len(st) != 0 :
        result.append("NO")

